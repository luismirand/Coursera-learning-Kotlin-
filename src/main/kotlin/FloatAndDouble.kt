fun main() {
    //floats and doubles
    //se diferencian por precision en decimales
    //double es mas preciso que float

    val myNumber = 2f //como float
    //para declarar un float ponemos f al final
    //el float siempre pone un .0 a los enteros
    val mySecondNumber = 2.0 // como double


    println("myNumber is $myNumber and mySecondNumber is $mySecondNumber")

}